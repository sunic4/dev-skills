#!/usr/bin/env node

import { readFileSync, writeFileSync, existsSync, mkdirSync, readdirSync } from 'fs';
import { resolve, dirname, join } from 'path';
import { simpleYamlParse, yamlStringify } from './yaml-utils.mjs';

function parseArgs() {
  const args = process.argv.slice(2);
  const opts = { feature: null, output: null, wikiRoot: null };
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--feature' && args[i + 1]) { opts.feature = args[++i]; }
    else if (args[i] === '--output' && args[i + 1]) { opts.output = args[++i]; }
    else if (args[i] === '--wiki' && args[i + 1]) { opts.wikiRoot = args[++i]; }
  }
  return opts;
}

function now() {
  return new Date().toISOString().replace('T', 'T').slice(0, 16);
}

function generateReviewReport(checklist, featureId) {
  const files = checklist?.files || [];
  const totalFiles = files.length;

  let totalAdded = 0;
  let totalRemoved = 0;
  for (const f of files) {
    if (f.changes && typeof f.changes === 'string') {
      const m = f.changes.match(/\+(\d+)/);
      const r = f.changes.match(/-(\d+)/);
      if (m) totalAdded += parseInt(m[1], 10);
      if (r) totalRemoved += parseInt(r[1], 10);
    }
  }

  let changeSize = 'small';
  if (totalAdded + totalRemoved > 500 || totalFiles > 8) changeSize = 'large';
  else if (totalAdded + totalRemoved > 100 || totalFiles > 3) changeSize = 'medium';

  return {
    meta: {
      feature_id: featureId,
      reviewer: 'agent-reviewer',
      reviewed_at: now(),
      verdict: 'request_changes'
    },
    summary: {
      total_files_changed: totalFiles,
      lines_added: totalAdded,
      lines_removed: totalRemoved,
      change_size: change_size
    },
    axes: {
      correctness: { score: 0, findings: [] },
      security: { score: 0, findings: [] },
      performance: { score: 0, findings: [] },
      maintainability: { score: 0, findings: [] },
      test_coverage: { score: 0, findings: [] }
    },
    action_items: []
  };
}

function findChecklist(featureId, wikiRoot) {
  const featuresDir = resolve(wikiRoot, 'features');
  if (!existsSync(featuresDir)) return null;
  try {
    const entries = readdirSync(featuresDir, { withFileTypes: true });
    for (const entry of entries) {
      if (!entry.isDirectory()) continue;
      if (!entry.name.includes(featureId)) continue;
      const candidate = join(featuresDir, entry.name, 'impl-checklist.yaml');
      if (existsSync(candidate)) return candidate;
    }
    return null;
  } catch {
    return null;
  }
}

function main() {
  const opts = parseArgs();

  if (!opts.feature) {
    console.log(`Usage: node review-generate.mjs --feature <slug> [--wiki <path>] [--output <path>]

Generates a review-report.yaml template for the given feature.
Reads impl-checklist.yaml to determine file list and change scope.

The generated template has empty findings — the agent fills in
actual review results after examining each axis.`);
    process.exit(0);
  }

  const wikiRoot = opts.wikiRoot || resolve(process.cwd(), 'wiki');

  let checklistData = null;
  let foundPath = findChecklist(opts.feature, wikiRoot);

  if (foundPath) {
    try {
      checklistData = simpleYamlParse(readFileSync(foundPath, 'utf-8'));
    } catch (e) {
      console.error(`ERROR: Failed to parse ${foundPath}: ${e.message}`);
      process.exit(1);
    }
  }

  const report = generateReviewReport(checklistData, opts.feature);

  if (checklistData) {
    console.log(`Read checklist from: ${foundPath}`);
    console.log(`Files changed: ${report.summary.total_files_changed}, Lines: +${report.summary.lines_added}/-${report.summary.lines_removed}`);
    console.log(`Change size: ${report.summary.change_size}`);
  } else {
    console.log(`WARN: No impl-checklist.yaml found for "${opts.feature}". Generating minimal template.`);
  }

  let outputPath;
  if (opts.output) {
    outputPath = opts.output;
  } else if (foundPath) {
    outputPath = join(dirname(foundPath), `${opts.feature}-review-report.yaml`);
  } else {
    const featuresDir = resolve(wikiRoot, 'features');
    const entries = existsSync(featuresDir) ? readdirSync(featuresDir) : [];
    const matchDir = entries.find(e => e.includes(opts.feature) && existsSync(join(featuresDir, e)));
    if (matchDir) {
      outputPath = join(featuresDir, matchDir, `${opts.feature}-review-report.yaml`);
    } else {
      outputPath = resolve(wikiRoot, 'features', `${opts.feature}-review-report.yaml`);
    }
  }

  const outputDir = dirname(outputPath);
  if (!existsSync(outputDir)) mkdirSync(outputDir, { recursive: true });

  const header = [
    '# Auto-generated by review-generate.mjs',
    '# Fill in scores and findings after reviewing each axis',
    '',
    ...yamlStringify(report).split('\n')
  ].join('\n');

  writeFileSync(outputPath, header, 'utf-8');
  console.log(`Generated: ${outputPath}`);
  console.log(`Next: Fill in axes.*.score (1-5) and axes.*.findings[] for each axis.`);
}

main().catch(e => {
  console.error(`FATAL: ${e.message}`);
  process.exit(1);
});
